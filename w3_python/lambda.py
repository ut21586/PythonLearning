#a lambda function is a small anonymous function
#a lambda function can take any number of arguements, but can only have 1 expression
#lambda syntax: lamda arguments: expression

l = lambda a: a + 10
print(l(5)) #should be print 15

x = lambda a, b : a * b
print(x(5, 6))

x = lambda a, b, c : a + b + c
print(x(5, 6, 2))

